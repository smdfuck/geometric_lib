import math
'''Import module math that keeps math constants and formulas'''

def area(r):
    '''Function get r radius of circle and return 2 * math.pi(3,14) * r^2
        - area'''
    return math.pi * r * r


def perimeter(r):
    '''Function get r radius and return 2 * math.pi(3,14) * r
        - perimeter of circle'''
    return 2 * math.pi * r

